package com.btc.hair.admin.services.hairinfo;

public class HairInfoDto {

    private int as_no;
    private String as_type;
    private int as_price;
    private String as_reg_date;
    private String as_mod_date;

    public int getAs_no() {
        return as_no;
    }

    public void setAs_no(int as_no) {
        this.as_no = as_no;
    }

    public String getAs_type() {
        return as_type;
    }

    public void setAs_type(String as_type) {
        this.as_type = as_type;
    }

    public int getAs_price() {
        return as_price;
    }

    public void setAs_price(int as_price) {
        this.as_price = as_price;
    }

    public String getAs_reg_date() {
        return as_reg_date;
    }

    public void setAs_reg_date(String as_reg_date) {
        this.as_reg_date = as_reg_date;
    }

    public String getAs_mod_date() {
        return as_mod_date;
    }

    public void setAs_mod_date(String as_mod_date) {
        this.as_mod_date = as_mod_date;
    }
}
